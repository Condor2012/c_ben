def calculate_burning_time(c1, b1):
    total_hours = 0
    potuhshie = 0

    # Изначально зажигаем все огоньки
    while c1 > 0:
        total_hours += c1 * 2  # Каждый огонек горит 2 часа
        potuhshie += c1  # После сгорания огоньки становятся потухшими
        c1 = 0  # Все огоньки потухли

        # Проверяем, можно ли сделать новые огоньки из потухших
        new_ogonki = potuhshie // b1
        potuhshie = potuhshie % b1
        c1 += new_ogonki  # Добавляем новые огоньки, которые можно зажечь

    return total_hours

# Пример использования
c1 = 10  # Количество начальных бенгальских огоньков
b1 = 5   # Количество потухших огоньков для создания 2 новых огоньков
print(calculate_burning_time(c1, b1))  # Вывод времени горения в часах
